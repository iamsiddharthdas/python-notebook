n = 2
while n<=10:
    print(n)
    n = n+2
'''

condition is n<=10
if true, it prints
if false, it doesnt print

so,
n=2       is <=10  -> true(prints 2)
n=2+2=4   is <=10  -> true(prints 4)
n=4+2=6   is <=10  -> true(prints 6)
n=6+2=8   is <=10  -> true(prints 8)
n=8+2=10  is <=10  -> true(prints 10)
n=10+2=12 is <=10  -> false (doesn't print)

output:
2
4
6
8
10

'''

