# Virtual Environment

'''
1. Install
2. Activate
3. pip install 3rd party modules(if needed)

Command to create virtual environment:
python -m venv folder_name
python3 -m venv folder_name (if not working in mac)

To activate:

- Windows: folder_name\Scripts\activate
(before activation, go to PowerShell terminal as an admin, 
and type 'Set-ExecutionPolicy Unrestricted')

- Linux:   source folder_name/bin/activate

To deactivate:
deactivate 

To find what modules are installed in the virtual environment folder:
pip list


To delete the virtual environment folder:
sudo rm -rf folder_name

'''




